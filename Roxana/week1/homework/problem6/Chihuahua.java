
public class Chihuahua extends Dog {

	public void mamifer(Mamifer m) {
		m.countryOfOrigin();
		m.whatTypeIs();
	}

	public void patruped(Patruped p) {
		p.color();
		p.numberOfLegs();
	}

	public void animal(Animal a) {
		a.age();
		a.dimensions();
	}

	public void animalDomestic(AnimalDomestic aD) {
		aD.countryOfOrigin();
		aD.whatTypeIs();
		aD.color();
		aD.numberOfLegs();
		aD.age();
		aD.dimensions();
		aD.displayingOwner();
	}

}
