public class Atention {
    String name;
    int value;

    public Atention(String name, int value) {
        this.name = name;
        this.value = value;
    }

    public int getValue() {
        return value;
    }

    public String getName() {
        return name;
    }

    public void Value(int value) {
        this.value = value;
    }
}
